//função para média aritimética
float arit(float num1, float num2, float num3)
{
    float m_arit;

    m_arit = (num1+num2+num3)/3;
    return m_arit;
}
//função para média ponderada
float pond(float num1, float num2, float num3)
{
    float m_pond;
    m_pond = (num1*5 + num2*3 + num3*2)/10;
    return m_pond;
}
